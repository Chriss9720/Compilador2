package Model;

/**
 *
 * @author Gonza
 */
public class Etiquetas {
    
    private String ini;
    private String fin;

    public Etiquetas() {
        this.ini = "";
        this.fin = "";
    }

    public String getIni() {
        return ini;
    }

    public void setIni(String ini) {
        this.ini = ini;
    }

    public String getFin() {
        return fin;
    }

    public void setFin(String fin) {
        this.fin = fin;
    }
    
    
}
