    private void contar() {
        Thread hilo = new Thread() {
            @Override
            public void run() {
                int i = 0;
                while (time) {
                    try {
                        Thread.sleep(1000);
                        ++i;
                        String t = "0000";
                        pantalla.getTiempo().setText(t);
                        switch (String.valueOf(i).length()) {
                            case 1:
                                t = "000" + i;
                                break;
                            case 2:
                                t = "00" + i;
                                break;
                            case 3:
                                t = "0" + i;
                                break;
                            default:
                                t = "" + i;
                        }
                        pantalla.getTiempo().setText(t);
                    } catch (InterruptedException e) {
                        System.out.println(e);
                    }
                }
            }
        };
        hilo.start();
    }

